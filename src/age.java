
public class age {
public static void main(String[] args) {
	int age = 10;
	System.out.println(age);
	System.out.println(age++);
	System.out.println(age);
	System.out.println(++age);
	System.out.println(age);
	int x = age++;
	System.out.println(x);
	x=x++;
	System.out.println(x);
	x++;
	System.out.println(x);
}
}
 